    private Vector3 CalculateToroidalCoordinates(int index)
    {
        // Projects discrete 64TG nodes across toroidal space angular ratios
        float angle = index * (Mathf.PI * 2 / totalNodes);
        return new Vector3(Mathf.Sin(angle) * 2.0f, 0, Mathf.Cos(angle) * 2.0f);
