public class FieldSynthesizer : MonoBehaviour
{
    [Header("Lattice Configuration")]
    public int totalNodes = 64;
    public float torqueGapThreshold = 5.0f;
    public float vacuumImpedanceAlpha = 137.035999143f;

    [Header("Simulation State")]
    [SerializeField] private int activeSethPhase = 0; // -1 = Inhale, 1 = Exhale

    void Update()
    {
        ExecuteIsentropicPhaseLoop();
    }

    private void ExecuteIsentropicPhaseLoop()
    {
        // Calculate the real-time, lossless breathing cycles of the system
        float dynamicOscillation = Mathf.Sin(Time.time * torqueGapThreshold);
        
        // Update the active Seth Phase loop vector
        activeSethPhase = (dynamicOscillation < 0) ? -1 : 1;

        for (int i = 0; i < totalNodes; i++)
        {
            // Fetch the Isotropic spatial coordinate
            Vector3 nodePosition = CalculateIsotropicVectorCoordinates(i);
            
            // Calculate the Isentropic energy magnitude
            float vectorMagnitude = activeSethPhase * (vacuumImpedanceAlpha / totalNodes);
            
            // Project the vectors radially (inward/outward) from the center
            Vector3 radialDirection = nodePosition.normalized;
            Debug.DrawRay(nodePosition, radialDirection * vectorMagnitude, activeSethPhase == 1 ? Color.cyan : Color.magenta);
        }
    }

    private Vector3 CalculateIsotropicVectorCoordinates(int index)
    {
        // Projects discrete 64TG nodes across true 3D spherical volume 
        // utilizing the Golden Ratio (Phi) for deterministic spatial distribution.
        
        float phi = 1.61803398875f;
        float goldenAngle = Mathf.PI * 2.0f * (2.0f - phi);
        
        // Map Y along the vertical axis from 1 to -1 to create 3D depth
        float y = 1.0f - (index / (float)(totalNodes - 1)) * 2.0f; 
        
        // Calculate the radius at this specific Y height
        float radiusAtY = Mathf.Sqrt(1.0f - y * y); 
        
        // Apply the Golden Angle to distribute the node radially
        float theta = goldenAngle * index; 

        // Return the true 3D vector coordinate (scaled by an arbitrary simulation radius of 2.0f)
        float x = Mathf.Cos(theta) * radiusAtY;
        float z = Mathf.Sin(theta) * radiusAtY;

        return new Vector3(x * 2.0f, y * 2.0f, z * 2.0f);
    }
}
